class KeyboardControl(object):
    """Class that handles keyboard input."""
    def __init__(self, world, start_in_autopilot, bypass_commands, tm_port):

        self._bypass_commands = bypass_commands
        self._last_key = ''
        self._autopilot_enabled = start_in_autopilot
        self._dmode = 0
        if self._autopilot_enabled:
            self._bypass_commands = False
        elif self._bypass_commands:
            self._dmode = 2
        else:
            self._dmode =1

        self._ackermann_enabled = False
        self._ackermann_reverse = 1
        if isinstance(world.player, carla.Vehicle):
            self._control = carla.VehicleControl()
            self._ackermann_control = carla.VehicleAckermannControl()
            self._lights = carla.VehicleLightState.NONE
            world.player.set_autopilot(self._autopilot_enabled, tm_port)
            world.player.set_light_state(self._lights)
        elif isinstance(world.player, carla.Walker):
            self._control = carla.WalkerControl()
            self._autopilot_enabled = False
            self._rotation = world.player.get_transform().rotation
        else:
            raise NotImplementedError("Actor type not supported")
        self._steer_cache = 0.0

    def get_last_key(self):
        return self._last_key

    def toggle_driving_mode(self):
        self._dmode = (self._dmode + 1) % 3

        if self._dmode == 0:
            self._autopilot_enabled = True
            self._bypass_commands = False
        elif self._dmode == 1:
            self._autopilot_enabled = False
            self._bypass_commands = False
        else:
            self._autopilot_enabled = False
            self._bypass_commands = True

    def get_driving_mode(self):
        if self._dmode == 0:
            return "autopilot"
        elif self._dmode == 1:
            return "manual"
        else:
            return "ai"


